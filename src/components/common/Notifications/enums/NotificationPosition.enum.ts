/**
 * @enum {string} Положение уведомления.
 * @member {NotificationPosition.rightTop} rightTop - Справа-сверху.
 * @member {NotificationPosition.rightBottom} rightBottom - Справа-снизу.
 * @member {NotificationPosition.leftTop} leftTop - Слева-сверху.
 * @member {NotificationPosition.leftBottom} leftBottom - Слева-снизу.
 * @member {NotificationPosition.topCenter} topCenter - Сверху по центру.
 * @member {NotificationPosition.bottomCenter} bottomCenter - Снизу по центру.
 * */
export enum NotificationPosition {
	rightTop = 'rightTop',
	rightBottom = 'rightBottom',
	leftTop = 'leftTop',
	leftBottom = 'leftBottom',
	topCenter = 'topCenter',
	bottomCenter = 'bottomCenter',
}
